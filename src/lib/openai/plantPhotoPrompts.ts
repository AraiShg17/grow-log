/** 登録名（テキスト）と写真の照合・正直な観察の共通指示 */

export function buildPhotoPlantVerificationRules(
  nameLabel: string,
  plantName: string,
): string {
  return `【ステップ1: 写真と${nameLabel}の照合（必ず最初に行う）】
対象の植物名はテキストで「${plantName}」と固定されています。写真は、この名前の植物の個体が写っているかを確認するためだけに使います。

次のいずれかに当てはまる場合、一般論・推定・好意的な健康評価で埋めてはいけません。
- 植物が写っていない、または植物として判別できない
- 写真の植物が「${plantName}」と**明らかに別種・無関係**
- 写真だけから「${plantName}」の個体であると十分に確信できない

照合失敗時**だけ**、ユーザー向けの本文に次を明記してください。
- 「写真からは『${plantName}』と判断できない」、または「登録名と一致しない」
- その理由（別種・非植物・判別困難など）を1文で添える

照合に**成功した場合**、照合結果の報告は**一切書かない**でください（内部判断にとどめる）。
次のような文は**禁止**です。
- 「写真から『${plantName}』と判断できる／一致している」
- ラベル・葉・茎が登録名と合うことの説明
- 照合を通過したことの宣言
ユーザーには、植物の状態とケアのアドバイスだけを書いてください。`;
}

/** 照合成功時に検証報告を出さない（観察・登録共通） */
export function buildSkipVerificationReportOnSuccessRules(): string {
  return `【照合成功時の出力ルール】
照合に通った場合、照合プロセスへの言及は不要です。ユーザーは照合の報告を求めていません。
「判断できます」「一致しています」「ラベルも〜」など、照合がうまくいったことの説明は書かず、いきなり観察内容・育成アドバイスに入ってください。`;
}

/** 肥料など「何を買うか」がわかる具体例（育成ガイド・観察アドバイス共通） */
export function buildFertilizerProductClarityRules(): string {
  return `【肥料・液肥など購入品を書くときのルール】
肥料・液肥・緩効性肥料・有機肥料・土の改良材などを**推奨・言及する場合**、「肥料をあげる」だけでは不十分です。初心者が「どれを買えばいいか」イメージできるよう、次を守ってください。

- **具体商品**: 日本で入手しやすい製品を1〜2点（メーカー名＋製品名。例: マグアンプK、ハイポネックス 原液 など）
- **用途の一言**: なぜその品がこの植物・時期向きか（観葉用・多肉用・有機・NPKの目安など）
- **購入URL**: Markdown リンク \`[表示名](https://...)\` で実在する販売ページを示す（Amazon.co.jp・楽天・メーカー公式・ホームセンター通販など）
- URLを特定できない場合は、実在が確実な検索ページ（例: \`[Amazonで「ハイポネックス 原液」を検索](https://www.amazon.co.jp/s?k=...)\`）を使う
- **捏造禁止**: 存在しない商品名・架空URL・短縮URLは使わない。確信が低い場合は商品名と店頭での選び方（「観葉植物用」「窒素控えめ」など）だけ書く
- 肥料の話題がない節では無理に商品例を入れない`;
}

export function buildHonestHealthAssessmentRules(options?: {
  includeMemoEmpathy?: boolean;
}): string {
  const memoRule =
    options?.includeMemoEmpathy !== false
      ? `- **メモへの共感・肯定的な反応**は、ユーザーの観察コメントへの短い返答に**限定**する。メモの内容を、健康状態が良い証拠にしない。\n`
      : `- 入力名だけから種類や育て方を断定しない。写真と入力名の両方に基づき、種類は「推定」として書く。\n`;

  return `${buildSkipVerificationReportOnSuccessRules()}

【ステップ2以降: 正直な記述（照合成功時のみ）】
- **健康状態**（葉・茎・土・全体）は、写真に見える根拠だけを述べる。登録名・育成ガイド・一般論から「元気そう」「問題なさそう」とは書かない。
${memoRule}- 気になる点は写真から読み取れる範囲で率直に書く。見えないことは「写真からは確認できない」と書く（これは照合報告ではなく、画面に写っていない部位の話として）。
- 確信が低いときは「〜の可能性」「〜と推定」と表現し、根拠のない断定は避ける。不確かさを、曖昧な好意的表現で隠さない。`;
}

export function buildLogAnalysisUnavailableMarkdown(plantName: string): string {
  return `## まとめ
- 写真からは「${plantName}」と判断できませんでした。登録名と写真の内容が一致しない、または植物として判別できない可能性があります。
- このため、状態の解析と具体的なアドバイスは行いませんでした。
- 対象の植物がはっきり写った写真で、もう一度記録してください。

## 詳細
### 現在の状態
解析できませんでした。テキストで登録されている「${plantName}」と今回の写真を照合しましたが、同一の個体とは判断できませんでした。一般論による健康評価は行っていません。

### 今すぐやること
該当なし（解析不可のため）

### 様子を見ること
該当なし（解析不可のため）

### 次回チェック
該当なし（解析不可のため）

### 注意点
別種・非植物・判別困難な写真では、アドバイスを生成しません。`;
}

